/**
 * @method Comment|null find($id, $lockMode = null, $lockVersion = null)
 * @method Comment|null findOneBy(array $criteria, array $orderBy = null)
 * @method Comment[]    findAll()
 * @method Comment[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
final class CommentRepository extends ServiceEntityRepository
{
    public const PAGINATOR_PER_PAGE = 5;

    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Comment::class);
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function add(Comment $entity, bool $flush = true): void
    {
        $this->_em->persist($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function remove(Comment $entity, bool $flush = true): void
    {
        $this->_em->remove($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    public function getCommentPaginator(Movie $movie, int $offset): Paginator
    {
        $query = $this->createQueryBuilder('c')
            ->andWhere('c.movie = :movie')
            ->setParameter('movie', $movie)
            ->setMaxResults(self::PAGINATOR_PER_PAGE)
            ->setFirstResult($offset)
            ->getQuery();

        return new Paginator($query);
    }

    public function getCommentsCount(UserInterface $author, Movie $movie): int
    {
        $date = new \DateTime();
        $date->modify('-10 minutes');

        return count($this->createQueryBuilder('c')
            ->andWhere('c.author = :author')
            ->andWhere('c.movie = :movie')
            ->andWhere('c.created_at > :date')
            ->setParameters(['author' => $author, 'movie' => $movie, 'date' => $date])
            ->getQuery()
            ->getResult()
        );
    }

